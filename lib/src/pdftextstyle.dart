/// This defines the textStyles available to the Document
/// To use a different style simple set custom values for
/// [title], [heading1], [heading2], [heading3] and [normal]
class PDFTextStyle {
  Map title = {"size": 20.0, "font": null};
  Map heading1 = {"size": 15.0, "font": null};
  Map heading2 = {"size": 12.0, "font": null};
  Map heading3 = {"size": 10.0, "font": null};
  Map normal = {"size": 10.0, "font": null};
}
